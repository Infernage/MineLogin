    private void jTextArea1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextArea1KeyPressed
        // TODO add your handling code here:
        //Escáner de teclado del Texto
        switch (evt.getKeyCode()){
            case KeyEvent.VK_A: egg.append("A");
            if(CHLG.length() == egg.length()){
                comprobarKey();
                egg.delete(0, egg.length());
            }
                break;
            case KeyEvent.VK_D: egg.append("D");
            if(CHLG.length() == egg.length()){
                comprobarKey();
                egg.delete(0, egg.length());
            }
            break;
            case KeyEvent.VK_M: egg.append("M");
            if(CHLG.length() == egg.length()){
                comprobarKey();
                egg.delete(0, egg.length());
            }
                break;
            case KeyEvent.VK_I: egg.append("I");
            if(CHLG.length() == egg.length()){
                comprobarKey();
                egg.delete(0, egg.length());
            }
                break;
            case KeyEvent.VK_N: egg.append("N");
            if(CHLG.length() == egg.length()){
                comprobarKey();
                egg.delete(0, egg.length());
            }
                break;
            case KeyEvent.VK_ENTER: modify();
                break;
        }
    }//GEN-LAST:event_jTextArea1KeyPressed

