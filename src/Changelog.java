    private void inicializar(){
        try{
            //Leemos el txt en formato Unicode
            FileInputStream in = new FileInputStream(System.getProperty("user.home") + "\\AppData\\Roaming\\.minecraft\\mods\\CHLog.txt");
            InputStreamReader isr = new InputStreamReader (in, "UTF8");
            BufferedReader bf = new BufferedReader (isr);
            String temp;
            jTextArea1.append(bf.readLine());
            while ((temp = bf.readLine()) != null){
                jTextArea1.append("\n");
                jTextArea1.append(temp);
            }
            in.close();
            isr.close();
            bf.close();
            jButton1.setEnabled(true);
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, "Error al abrir el ChangeLog.");
            File error = new File (System.getProperty("user.home") + "\\AppData\\Roaming\\Data\\LogEr.txt");
            if (!error.exists()){
                try {
                    error.createNewFile();
                } catch (IOException exe) {
                }
            }
                try{
                    PrintWriter pw = new PrintWriter (error);
                    pw.print(e.getMessage());
                    pw.println();
                    pw.close();
                } catch (IOException exe){
                }
        }
    }
    private void irArriba(){
        //Método ejecutado por el Botón de ir arriba
        JViewport v = jScrollPane1.getViewport();
        Point p = v.getViewPosition();
        p.setLocation(0, 0);
        v.setViewPosition(p);
        jScrollPane1.setViewport(v);
    }
    private void comprobarKey() {
        //Escáner del teclado que comprueba si se ejecuta el huevo de pascua
        if (CHLG.equalsIgnoreCase(egg.toString())){
            jButton3.setVisible(true);
            jButton3.setBorderPainted(true);
            jButton3.setContentAreaFilled(true);
            jButton3.setEnabled(true);
            modify();
        }
    }
    private void modify(){
        //Método de huevo de pascua que permite la edición del ChangeLog
        if (jTextArea1.isEditable()){
            try{
                PrintWriter pw = new PrintWriter (new File (System.getProperty("user.home") + "\\AppData\\Roaming\\.minecraft\\mods\\CHLog.txt"));
                String temp = jTextArea1.getText();
                String [] splitter = temp.split("\n");
                for (int i = 0; i < splitter.length; i++){
                    pw.print(splitter[i]);
                    pw.println(); 
                }
                pw.close();
            } catch (IOException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else{
            jTextArea1.setEditable(true);
        }
    }
